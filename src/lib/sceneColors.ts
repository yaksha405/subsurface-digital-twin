// ===================================================================
// 八、科学数据色图（Turbo Colormap — 数据叠色全局统一）
// ===================================================================

/**
 * 数值→颜色映射，采用改进的 turbo colormap
 * 蓝(低) → 青 → 绿 → 黄 → 红(高)，超阈值为红橙
 *
 * 这是所有场景传感器数据着色的统一标准。
 * 不同场景通过 getSensorMetric() 定义各自的 value/min/max/threshold。
 */
export function valueToColor(
  value: number,
  min: number,
  max: number,
  threshold?: number,
): { r: number; g: number; b: number } {
  const t = Math.max(0, Math.min(1, (value - min) / (max - min)));
  let r: number, g: number, b: number;

  if (threshold !== undefined && value >= threshold) {
    // 超阈值：红色警告
    r = 1.0;
    g = 0.2 + (1 - t) * 0.3;
    b = 0.1;
  } else if (t < 0.33) {
    // 蓝 → 青
    r = 0.1;
    g = 0.5 + t * 1.5;
    b = 0.9 - t * 0.5;
  } else if (t < 0.66) {
    // 青 → 绿 → 黄
    const lt = (t - 0.33) / 0.33;
    r = lt * 1.0;
    g = 0.85;
    b = 0.3 - lt * 0.2;
  } else {
    // 黄 → 橙 → 红
    const lt = (t - 0.66) / 0.34;
    r = 1.0;
    g = 0.85 - lt * 0.65;
    b = 0.1;
  }

  return { r, g, b };
}

